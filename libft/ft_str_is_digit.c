#include "libft.h"

int		ft_str_is_digit(char *s)
{
	int		i;

	i = 0;
	if (!s || (*s != '-' && *s != '+' && (*s < '0' || *s > '9')))
		return (0);
	while (s[++i])
		if (s[i] < '0' || s[i] > '9')
			return (0);
	return (1);
}
